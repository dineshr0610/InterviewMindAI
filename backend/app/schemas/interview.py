class EndInterviewResponse(BaseModel):
    """
    Response schema for ending an interview.
    """

    status: str = Field(
        default="completed",
        description="Status confirmation",
    )
    interview_id: UUID = Field(
        ...,
        description="UUID of the ended interview",
    )
